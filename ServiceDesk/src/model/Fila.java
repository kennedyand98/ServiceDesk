package model;

public class Fila {
	
	//PARAMETROS
	int id;
	String nomeFila;
	
	
	//GETS E SETS
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNomeFila() {
		return nomeFila;
	}
	public void setNomeFila(String nomeFila) {
		this.nomeFila = nomeFila;
	}
	

}
